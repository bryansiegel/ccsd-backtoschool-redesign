                </h2>
                <div id="panelsStayOpen-collapse2" class="accordion-collapse collapse"
                     aria-labelledby="panelsStayOpen-heading2">
                    <div class="accordion-body">
                        <p>Children must meet the following minimum age and entrance requirements on or
                            before August 1 to be admitted to kindergarten, first, or second grade
                            (Nevada Revised Statutes [NRS] 392.040):</p>
                        <p><strong>✔ Kindergarten Students</strong> must have attained the age of five
                            years old by August 1 to attend kindergarten.</p>

                        <p><strong>✔ Grade 1 Students</strong> must have attained the age of six years
                            old by August 1 and demonstrate proof of completing a minimum of six weeks
                            of kindergarten at a public, charter, or private school. The kindergarten
                            requirement cannot be met via online or a homeschool program. (A student who
                            is enrolled in and successfully completes kindergarten may be promoted to
                            the next grade level regardless of their age during the 2024–2025 school
                            year.)
                        <p align="center">OR</p>
                        <p><strong>✔</strong> Demonstrate proof of completing kindergarten and promotion
                            to first grade while being a resident of another state/country in compliance
                            with the age and entrance requirements of that state/country. Proof must be
                            verified through a report card issued by the last out-of-district public,
                            charter or private kindergarten.</p>
                        <p>Children who have attained the age of six who do not meet the entrance
                            requirements of Grade 1 will be placed in kindergarten. </p>

                        <p><strong>✔ Grade 2 Students</strong> must have attained the age of seven years
                            old and demonstrate proof of completing kindergarten and first grade at a
                            public, charter, or private school. (A student who is enrolled in and
                            successfully completes first grade may be promoted to the next grade level
                            regardless of their age during the 2024–2025 school year.)</p>

                        <p>Children who have attained the age of seven who do not meet the entrance
                            requirements of Grade 2 will be assessed to determine if the student is
                            developmentally ready for first or second grade.</p>

                        <p>Children in Nevada between the ages of six and 18 must be enrolled in and
                            attend school for the entire time school is in session, pursuant to NRS
                            392.040.</p>

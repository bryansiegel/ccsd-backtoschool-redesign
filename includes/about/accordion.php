                        <p><strong>1. Proof of Address</strong>: One form of proof of address is
                            required. A recent utility bill (telephone and cable bills are not
                            acceptable), current mortgage statement, rental receipt, residential lease
                            agreement, or sales contract are acceptable forms of proof of address. </p>

                        <p><strong>2. Parent/Guardian Personal Identification</strong>: A driver’s
                            license, picture identification, and/or passport are acceptable forms of
                            personal identification. Permanent and temporary guardians should be
                            prepared to provide proof of guardianship at the time of registration. </p>

                        <p><strong>3. Proof of Child’s Identity</strong>: An original birth certificate,
                            passport, or certified birth card issued by a health district are acceptable
                            documents for proof of a child's identity. </p>

                        <p><strong>4. Immunizations: Nevada law requires public school students to be
                                immunized. Parents/Guardians also must provide an appropriate valid
                                certificate with the documentation. Immunization requirements are
                                outlined in NRS 392.435.</strong></p>

                        <p>Unless excused because of religious belief or medical condition, a child may
                            not be enrolled in a public school within Nevada unless the child has been
                            immunized and has received the proper boosters for that immunization or is
                            complying with the schedules established pursuant to NRS 439.550.</p>

                        <p>For detailed information concerning immunization requirements for children
                            attending CCSD schools, visit <a
                                href="https://ccsd.net/parents/enrollment#proofImm" target="_blank">Student
                                Enrollment Information</a>.</p>

                        For assistance with obtaining immunizations, parents/guardians may contact the
                        Southern Nevada Health District at <a href="tel:(702)-759-0850">702-759-0850</a>.</p>
